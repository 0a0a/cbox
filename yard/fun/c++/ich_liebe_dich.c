void show(int (*arr)[MCOL], int row, int col, FILE* fp)
{
    int i = 0, j = 0;
    char c = 0;
    for (i = 0; i < row; ++i)
    {
        for (j = 0; j < col; ++j)
        {
            c = (arr[i][j] != 0)? '*': ' ';
            fputc(c, fp);
        }
        fputc('\n', fp);
    }
}


